#include "3-calc.h"

/**
 * op_add - add tow numbers
 * @a: the first number
 * @b: the second number
 * Return: addition of tow numbers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract tow numbers
 * @a: the first number
 * @b: the second number
 * Return: subtraction of tow numbers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply tow numbers
 * @a: the first number
 * @b: the second number
 * Return: multiplication of tow numbers
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide tow numbers
 * @a: the first number
 * @b: the second number
 * Return: division of tow numbers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the reset of the dvision of tow numbers
 * @a: the first number
 * @b: the second number
 * Return: modulo of tow numbers
*/
int op_mod(int a, int b)
{
	return (a % b);
}

